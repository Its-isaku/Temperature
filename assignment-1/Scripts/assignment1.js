// 1. Declare Variables
// Using let
let character = "Tom";
let action = "laugh";
let object = "a talking pineapple";
let place = "the library";
let time = "a stormy night";

// Using const
const occasion = "a very wild dream";

// 2. Create the Story
console.log("--- Funny Story ---");
console.log(character + " was at " + place + " on " + time + ".");
console.log("Suddenly, " + character + " saw " + object + " and started to " + action + ".");
console.log("It turned out to be " + occasion + " and then he woke up!");

// 3. Experiment with Different Values
